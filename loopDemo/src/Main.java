public class Main {
    public static void main(String[] args) {

        //for

        for(int i=2;i<10;i+=2) {
            System.out.println(i);
        }

        System.out.println("for Dongusu Bitti");
        int i=2;
        //While
        while(i<10){
            System.out.println(i);
            i+=2;
        }
        System.out.println("while Dongusu Bitti");
    }
}